public class Desk {
  String material;
  int numberOfDrawers;
  
  public Desk() {
    material  = "oak";
    numberOfDrawers = 5;
  }  
  public Desk(String material, int numberOfDrawers) {
    this.material =  material;
    this.numberOfDrawers = numberOfDrawers;
  } 
}
