class Chair {
  String colour;
  boolean rolling;
  boolean padded;
  public Chair(String colour, boolean rolling, boolean padded) { 
    this.colour = colour;
    this.rolling = rolling;
    this.padded = padded;
  }
  public Chair() { 
    colour = "green";
    rolling = false;
    padded = true;
  }
}
